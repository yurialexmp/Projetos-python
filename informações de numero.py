n=(int(input('digite um número:')))
d=n*2
t=n*3
q=n*4
r=n**(1/2)
rc=n**(1/3)
print('o dobro de:{} é:{}'.format(n,d))
print('o triplo de:{} é:{}'.format(n,t))
print('o quadruplo de:{} é:{}'.format(n,q))
print('a raiz quadrada de:{} é: {:.2f}'.format(n,r))
print('a raiz cúbica de:{} é:{:.2f}'.format(n,rc))


